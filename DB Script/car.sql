create table cars.car(
  car_id int primary key,
  brand varchar(50),
  model varchar(50),
  [year] varchar(10),
  body varchar (30),
  transmission varchar(30),
  fuel_type varchar(30),
  engine_capacity varchar(20),
  mileage varchar(25),
  color varchar (25),
  weight varchar(20),
  doors varchar(20),
  wheel_size varchar(20)
  )
